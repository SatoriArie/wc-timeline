// Космология Warcraft — шесть фундаментальных сил мироздания,
// существующих тремя противоборствующими парами вокруг Великой Запредельной Тьмы.

export interface CosmicForce {
  id: string;
  name: string;
  /** Краткий эпитет силы */
  epithet: string;
  /** CSS-цвет силы */
  color: string;
  /** Суть силы */
  essence: string;
  /** Воплощения и носители */
  embodiments: string[];
  /** Магическая школа/практики */
  school: string;
}

export interface CosmicPair {
  id: string;
  title: string;
  /** Что определяет противостояние пары */
  axis: string;
  light: CosmicForce; // условно «светлая»/созидательная сторона
  dark: CosmicForce; // условно «тёмная»/разрушительная сторона
}

